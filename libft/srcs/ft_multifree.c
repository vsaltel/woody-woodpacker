#include "libft.h"

void	ft_multifree(char **s1, char **s2, char **s3)
{
	if (s1 && *s1)
		ft_strdel(s1);
	if (s2 && *s2)
		ft_strdel(s2);
	if (s3 && *s3)
		ft_strdel(s3);
}
